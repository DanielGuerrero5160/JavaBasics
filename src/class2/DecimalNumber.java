package class2;

public class DecimalNumber {
    public static void main(String[] args) {
        /*
        If we want to store decimal numbers in computers in java,
        we can use the following boxes.
        1)float - Needs f after number -up to 6 to 7 decimal digits
        2)double - up to 15 decimal digits
         */
        float myNumberBox=10.5f;
        float mySecondBox=5.5f;
        double largeDecimalBox=121565.1256;
        System.out.println(myNumberBox);

        char letters='s';
        char gender='m';
        char symbol='#';
        char numberChar='1';
        /*
        all the data-types above are primitive data types.
        Strings are non-primitive which is why the rules are different
        Strings start with a capital S
         */
        String name="Daniel";
        boolean hungry=true;
        boolean sleepy=false;

    }
}
