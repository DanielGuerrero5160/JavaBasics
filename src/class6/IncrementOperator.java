package class6;

public class IncrementOperator {
    public static void main(String[] args) {

        int number=10;
        number=number+1;
        System.out.println(number);

        //same thing below
        number+=1;
        System.out.println(number);
        //same thing below
        number++;
        System.out.println(number);
        number--;// subtracting 1
        System.out.println(number);

    }
}
